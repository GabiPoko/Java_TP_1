package poo.desafio.simulador;

public class Examen {
    private int numero;
    private int puntaje;

    
    public Examen(int numero, int puntaje) {
        this.numero = numero;
        this.puntaje = puntaje;
    }


    public int getNumero() {
        return numero;
    }


    public int getPuntaje() {
        return puntaje;
    }

    

}
