public class obtenerPorcentaje {
    public static void main(String[] args) {
        
        double numero1 = 100;

        int numero2 = 25;

        double porcentaje = numero1 * (numero2 / 100.0); 

        System.out.println("El " + numero2 + " % de " + numero1 + " es " + porcentaje);
    }
}
